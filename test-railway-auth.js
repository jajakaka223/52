function testEndpoint(path, callback) {
  const options = {
    hostname: '52express-transport-app-production.up.railway.app',
    port: 443,
    path: path,
    method: 'GET',
  };

  const req = https.request(options, (res) => {
    let data = '';
    res.on('data', (chunk) => {
      data += chunk;
    });
    
    res.on('end', () => {
      callback(res, data);
    });
  });

  req.on('error', (e) => {
    console.error('Error:', e.message);
  });

  req.end();
}

